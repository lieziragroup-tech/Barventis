-- Otomatis sinkronisasi user profile ketika user baru mendaftar via Supabase Auth
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
DECLARE
    v_tenant_id UUID;
    v_tenant_name VARCHAR;
    v_company_name VARCHAR;
    v_role user_role;
BEGIN
    -- Ambil metadata pendaftaran
    v_tenant_name  := COALESCE(new.raw_user_meta_data->>'tenant_name', new.raw_user_meta_data->>'company_name', 'default-resto');
    v_company_name := COALESCE(new.raw_user_meta_data->>'company_name', v_tenant_name);
    v_role         := COALESCE((new.raw_user_meta_data->>'role')::user_role, 'Admin / Owner'::user_role);

    -- Cari tenant berdasarkan nama atau buat baru jika registrasi pertama
    SELECT id INTO v_tenant_id FROM public.tenants WHERE name = LOWER(v_tenant_name);
    
    IF v_tenant_id IS NULL THEN
        -- Hubungkan ke template default saat registrasi pertama
        INSERT INTO public.tenants (name, company_name, status, pos_template_id)
        VALUES (
            LOWER(v_tenant_name), 
            v_company_name, 
            'active', 
            (SELECT id FROM public.pos_templates WHERE name = 'UMATIS_DEFAULT' LIMIT 1)
        )
        RETURNING id INTO v_tenant_id;
    END IF;

    -- Tambahkan user profile
    INSERT INTO public.users (id, tenant_id, name, email, role)
    VALUES (
        new.id,
        v_tenant_id,
        COALESCE(new.raw_user_meta_data->>'name', 'User Resto'),
        new.email,
        v_role
    );
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

