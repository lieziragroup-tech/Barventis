-- Function 1: Atomic Invoice Receive
-- Ensures all stock updates + transaction inserts succeed or all roll back
CREATE OR REPLACE FUNCTION receive_invoice_atomic(
    p_invoice_id UUID,
    p_tenant_id UUID,
    p_user_id UUID
)
RETURNS JSON
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_invoice RECORD;
    v_item RECORD;
    v_material RECORD;
    v_location TEXT;
    v_now TEXT;
    v_processed INT := 0;
BEGIN
    -- Lock invoice row
    SELECT * INTO v_invoice
    FROM public.invoices
    WHERE id = p_invoice_id AND tenant_id = p_tenant_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'Invoice tidak ditemukan: %', p_invoice_id;
    END IF;
    IF v_invoice.status = 'RECEIVED' THEN
        RAISE EXCEPTION 'Invoice sudah pernah diterima';
    END IF;
    IF v_invoice.status = 'CANCELLED' THEN
        RAISE EXCEPTION 'Tidak bisa memproses invoice yang dibatalkan';
    END IF;

    v_location := COALESCE(v_invoice.location, 'CENTRAL');
    v_now := CURRENT_DATE::TEXT;

    -- Process each invoice item atomically
    FOR v_item IN
        SELECT ii.*, m.qty_resto, m.qty_central, m.new_price, m.price, m.name
        FROM public.invoice_items ii
        JOIN public.materials m ON m.id = ii.material_id
        WHERE ii.invoice_id = p_invoice_id
    LOOP
        -- Update stock
        IF v_location = 'RESTO' THEN
            UPDATE public.materials
            SET qty_resto = qty_resto + v_item.qty,
                new_price = v_item.unit_price,
                updated_at = NOW()
            WHERE id = v_item.material_id;
        ELSE
            UPDATE public.materials
            SET qty_central = qty_central + v_item.qty,
                new_price = v_item.unit_price,
                updated_at = NOW()
            WHERE id = v_item.material_id;
        END IF;

        -- Insert transaction ledger entry
        INSERT INTO public.transactions (tenant_id, date, material_id, type, location, qty, amount, notes, created_by)
        VALUES (
            p_tenant_id, v_now, v_item.material_id, 'PURCHASE_IN', v_location::stock_location,
            v_item.qty,
            v_item.qty * v_item.unit_price,
            'Auto stock-in from invoice: ' || v_invoice.invoice_no,
            p_user_id
        );

        v_processed := v_processed + 1;
    END LOOP;

    -- Update invoice status
    UPDATE public.invoices
    SET status = 'RECEIVED', received_date = v_now, updated_at = NOW()
    WHERE id = p_invoice_id;

    RETURN json_build_object(
        'success', true,
        'invoice_no', v_invoice.invoice_no,
        'items_processed', v_processed,
        'location', v_location
    );
END;
$$;

-- Function 2: Atomic Stock Opname Completion
CREATE OR REPLACE FUNCTION complete_opname_atomic(
    p_opname_id UUID,
    p_tenant_id UUID,
    p_location TEXT,
    p_user_id UUID
)
RETURNS JSON
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_item RECORD;
    v_variance NUMERIC;
    v_adjustments INT := 0;
    v_now TEXT;
BEGIN
    v_now := CURRENT_DATE::TEXT;

    -- Process each opname item
    FOR v_item IN
        SELECT soi.*, m.qty_resto, m.qty_central, m.new_price, m.price, m.name
        FROM public.stock_opname_items soi
        JOIN public.materials m ON m.id = soi.material_id
        WHERE soi.opname_id = p_opname_id
    LOOP
        IF p_location = 'RESTO' THEN
            v_variance := v_item.physical_qty - v_item.book_qty;
        ELSE
            v_variance := v_item.physical_qty - v_item.book_qty;
        END IF;

        IF ABS(v_variance) > 0.001 THEN
            -- Adjust material qty
            IF p_location = 'RESTO' THEN
                UPDATE public.materials
                SET qty_resto = v_item.physical_qty, updated_at = NOW()
                WHERE id = v_item.material_id;
            ELSE
                UPDATE public.materials
                SET qty_central = v_item.physical_qty, updated_at = NOW()
                WHERE id = v_item.material_id;
            END IF;

            -- Insert adjustment transaction
            INSERT INTO public.transactions (tenant_id, date, material_id, type, location, qty, amount, notes, created_by)
            VALUES (
                p_tenant_id, v_now, v_item.material_id, 'OPNAME_ADJ', p_location::stock_location,
                v_variance,
                v_variance * COALESCE(v_item.new_price, v_item.price, 0),
                'Stock Opname adjustment (Physical: ' || v_item.physical_qty || ', System: ' || v_item.book_qty || ')',
                p_user_id
            );

            v_adjustments := v_adjustments + 1;
        END IF;
    END LOOP;

    -- Mark opname as approved
    UPDATE public.stock_opnames
    SET status = 'APPROVED', approved_by = p_user_id, approved_at = NOW()
    WHERE id = p_opname_id;

    RETURN json_build_object(
        'success', true,
        'opname_id', p_opname_id,
        'adjustments_made', v_adjustments
    );
END;
$$;

-- Function 3: Check if a period is locked for a tenant
CREATE OR REPLACE FUNCTION is_period_locked(
    p_tenant_id UUID,
    p_year INT,
    p_month INT
)
RETURNS BOOLEAN
LANGUAGE plpgsql
STABLE SECURITY DEFINER
AS $$
DECLARE
    v_locked_year INT;
    v_locked_month INT;
BEGIN
    SELECT locked_until_year, locked_until_month
    INTO v_locked_year, v_locked_month
    FROM public.tenants WHERE id = p_tenant_id;

    IF v_locked_year IS NULL THEN RETURN FALSE; END IF;

    -- Period is locked if it's before or equal to the lock boundary
    RETURN (p_year < v_locked_year)
        OR (p_year = v_locked_year AND p_month <= v_locked_month);
END;
$$;

